package com.benedicto.mis.beans.containers;

public class Facility {
	private int id;
	private String type;
	private String description;
	private String facil_name;
	private String _switch;
	private String dateCreated;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the facil_name
	 */
	public String getFacil_name() {
		return facil_name;
	}
	/**
	 * @param facil_name the facil_name to set
	 */
	public void setFacil_name(String facil_name) {
		this.facil_name = facil_name;
	}
	/**
	 * @return the _switch
	 */
	public String get_switch() {
		return _switch;
	}
	/**
	 * @param _switch the _switch to set
	 */
	public void set_switch(String _switch) {
		this._switch = _switch;
	}
	/**
	 * @return the dateCreated
	 */
	public String getDateCreated() {
		return dateCreated;
	}
	/**
	 * @param dateCreated the dateCreated to set
	 */
	public void setDateCreated(String dateCreated) {
		this.dateCreated = dateCreated;
	}
	
	
	
}
