package com.benedicto.mis.beans.containers;

public class EvaluationProfile {
	
	private String id;
	private String verification;
	private boolean evaluated;
	private String date;
	private int evaluationNo;
	private int enrollmentNo;
	private int studentNo;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the verification
	 */
	public String getVerification() {
		return verification;
	}
	/**
	 * @param verification the verification to set
	 */
	public void setVerification(String verification) {
		this.verification = verification;
	}
	/**
	 * @return the evaluated
	 */
	public boolean isEvaluated() {
		return evaluated;
	}
	/**
	 * @param evaluated the evaluated to set
	 */
	public void setEvaluated(boolean evaluated) {
		this.evaluated = evaluated;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the evaluationNo
	 */
	public int getEvaluationNo() {
		return evaluationNo;
	}
	/**
	 * @param evaluationNo the evaluationNo to set
	 */
	public void setEvaluationNo(int evaluationNo) {
		this.evaluationNo = evaluationNo;
	}
	/**
	 * @return the enrollmentNo
	 */
	public int getEnrollmentNo() {
		return enrollmentNo;
	}
	/**
	 * @param enrollmentNo the enrollmentNo to set
	 */
	public void setEnrollmentNo(int enrollmentNo) {
		this.enrollmentNo = enrollmentNo;
	}
	/**
	 * @return the studentNo
	 */
	public int getStudentNo() {
		return studentNo;
	}
	/**
	 * @param studentNo the studentNo to set
	 */
	public void setStudentNo(int studentNo) {
		this.studentNo = studentNo;
	}
	
	
	
	
	
}
